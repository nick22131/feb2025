public class Main {
    public static void main(String[] args){
        // Declare and initialize two integer variables, x and y
        int x = 1; // x is assigned the value 1
        int y = 2; // y is assigned the value 2

        // Check if x is not equal to y using the != operator
        // The != operator returns true if the two values are not equal, otherwise false
        boolean result = x != y; // Since x is 1 and y is 2, result will be true

        // Print the result of the comparison
        System.out.println(result); // Output: true
    }
}
